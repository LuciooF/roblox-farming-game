-- Helper function to get the most appropriate icon variant
function IconAssets.getIcon(category, name, style)
    style = style or "flat" -- Default to flat style
    
    local categoryData = IconAssets[category]
    if not categoryData then return nil end
    
    local iconData = categoryData[name]
    if not iconData then return nil end
    
    -- If it's a simple icon with direct styles
    if type(iconData[style]) == "string" then
        return iconData[style]
    end
    
    -- If it has sub-categories (like arrow.left.flat)
    -- This is handled by direct access like: IconAssets.buttons.arrow.left.flat
    
    return nil
end

